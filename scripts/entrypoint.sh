#!/bin/sh
# Скрипт запуска бота: миграции → начальные данные → старт бота.
# Запускается автоматически через docker-compose.
set -e

echo ""
echo "====================================================="
echo "  Altairika Bot — подготовка к запуску"
echo "====================================================="

# 1. Применяем миграции БД
echo ""
echo "[1/4] Применяем миграции базы данных..."
alembic upgrade head
echo "      Миграции применены."

# 2. Заполняем FAQ (статические данные, всегда безопасно)
echo ""
echo "[2/4] Заполняем FAQ..."
python scripts/seed_faq.py || echo "      WARN: seed_faq завершился с ошибкой, пропускаем."

# 3. Заполняем таблицу сравнения с конкурентами (статические данные)
echo ""
echo "[3/4] Заполняем сравнение с конкурентами..."
python scripts/seed_compare.py || echo "      WARN: seed_compare завершился с ошибкой, пропускаем."

# 4. Парсим контент франшизы с сайта (требует интернет, необязательно)
echo ""
echo "[4/4] Синхронизируем контент франшизы с altairika.ru..."
python scripts/seed_franchise.py || echo "      WARN: seed_franchise завершился с ошибкой (сайт недоступен?), пропускаем."

echo ""
echo "====================================================="
echo "  Запускаем бота..."
echo "====================================================="
echo ""

exec python -m bot

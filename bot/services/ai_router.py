def detect_intent(text: str) -> str:
    """Определяет intent по тексту пользователя. Возвращает строку-ключ."""
    lower = text.lower()
    for intent, keywords in _INTENT_RULES:
        if any(kw in lower for kw in keywords):
            return intent
    return _DEFAULT_INTENT
